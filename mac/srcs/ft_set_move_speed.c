void	set_move_speed(t_all *a)
{
	a->mv_spd = fmax(0.08, \
			(double)(a->e->win_x * a->e->win_y) / (5 *  my_power(10, 6)));
	a->rt_spd = fmax(0.08, \
			(double)(a->e->win_x * a->e->win_y) / (10 *  my_power(10, 6)));
}
